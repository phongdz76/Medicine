    private String generateId() {
    Random random = new Random();
    int number = random.nextInt(1000); // Tạo số ngẫu nhiên từ 0 đến 999
    return String.format("%03d", number); // Chuyển số thành chuỗi 3 ký tự, thêm số 0 vào trước nếu cần
    }
